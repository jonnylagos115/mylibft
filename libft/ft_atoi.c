#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int sign;
	int n;

	i = 0;
	n = 0;
	sign = 1;
	if (!str)
		return ('\0');
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
			|| str[i] == '\f' || str[i] == '\r' || str[i] == '\v')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
		n = (n * 10) + (str[i++] - '0');
	return (n * sign);
}
